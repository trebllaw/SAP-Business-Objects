SELECT
  ADS_EVENT.EVENT_ID,
  ADS_EVENT_TYPE_STR.EVENT_TYPE_NAME,
  CASE ADS_STATUS_STR.STATUS_ID WHEN 0 THEN 'successful' ELSE 'failed' END,
  ADS_STATUS_STR.STATUS_NAME,
  ADS_EVENT.START_TIME,
  ADS_EVENT.DURATION_MS,
  ADS_EVENT.OBJECT_NAME,
  ADS_EVENT_TYPE_STR.EVENT_TYPE_ID,
  ADS_OBJECT_TYPE_STR.OBJECT_TYPE_NAME,
  ADS_EVENT.USER_NAME,
  cast(to_char(ADS_EVENT.START_TIME, 'HH24') as number),
  to_char(ADS_EVENT.START_TIME)
FROM
  ADS_EVENT,
  ADS_EVENT_TYPE_STR,
  ADS_EVENT_TYPE,
  ADS_STATUS_STR,
  ADS_OBJECT_TYPE_STR
WHERE
  ( ADS_EVENT.EVENT_TYPE_ID=ADS_EVENT_TYPE.EVENT_TYPE_ID  )
  AND  ( ADS_EVENT_TYPE.EVENT_TYPE_ID=ADS_EVENT_TYPE_STR.EVENT_TYPE_ID  )
  AND  ( ADS_EVENT.EVENT_TYPE_ID=ADS_STATUS_STR.EVENT_TYPE_ID and ADS_EVENT.STATUS_ID=ADS_STATUS_STR.STATUS_ID  )
  AND  ( ADS_EVENT.OBJECT_TYPE_ID=ADS_OBJECT_TYPE_STR.OBJECT_TYPE_ID  )
  AND  ( ADS_EVENT_TYPE_STR.LANGUAGE = 'EN'  )
  AND  ( ADS_OBJECT_TYPE_STR.LANGUAGE = 'EN'  )
  AND  ( ADS_STATUS_STR.LANGUAGE = 'EN'  )
  AND  
  (
   ADS_EVENT.USER_NAME  Is Not Null  
   AND
   ( TRUNC(ADS_EVENT.START_TIME) = TRUNC(SYSDATE)  )
  )


